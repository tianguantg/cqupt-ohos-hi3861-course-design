void OledGpioInit(void)
{
    u32 ret = KP_ERR_SUCCESS;
   
    //连接OLED屏幕的CS片选引脚，负责使能OLED屏幕
	ret = IoTGpioInit(OLED_NAME_GPIO_8);
	if(ret != KP_ERR_SUCCESS){ 
        printf("IoTGpioInit failed :%#x \r\n",ret);
		return;
    }

	ret = IoTGpioSetFunc(OLED_NAME_GPIO_8, IOT_GPIO_FUNC_GPIO_8_GPIO);
	if(ret != KP_ERR_SUCCESS){ 
        printf("IoTGpioSetFunc failed :%#x \r\n",ret);
		return;
    }
	
	ret = IoTGpioSetDir(OLED_NAME_GPIO_8, IOT_GPIO_DIR_OUT);
    if (ret != KP_ERR_SUCCESS) {
        printf("IoTGpioSetDir failed :%#x \r\n",ret); 
        return;
    }

    //连接OLED屏幕的DC引脚，负责对OLED屏幕进行指令控制
	ret = IoTGpioSetFunc(OLED_NAME_GPIO_11, IOT_GPIO_FUNC_GPIO_11_GPIO);
	if(ret != KP_ERR_SUCCESS){ 
        printf("IoTGpioSetFunc failed :%#x \r\n",ret);
		return;
    }
    
	ret = IoTGpioSetDir(OLED_NAME_GPIO_11, IOT_GPIO_DIR_OUT);
    if (ret != KP_ERR_SUCCESS) {
        printf("IoTGpioSetDir failed :%#x \r\n",ret); 
        return;
    }
	
    //连接OLED屏幕的RES引脚，负责复位OLED屏幕
	ret = IoTGpioSetFunc(OLED_NAME_GPIO_12, IOT_GPIO_FUNC_GPIO_12_GPIO);
	if(ret != KP_ERR_SUCCESS){ 
        printf("IoTGpioSetFunc failed :%#x \r\n",ret);
		return;
    }
    
	ret = IoTGpioSetDir(OLED_NAME_GPIO_12, IOT_GPIO_DIR_OUT);
    if (ret != KP_ERR_SUCCESS) {
        printf("IoTGpioSetDir failed :%#x \r\n",ret); 
        return;
    }

	printf("oled io init success \r\n");
}

void OledSpiInit(IotSpiIdx spiId){
    u32 ret = KP_ERR_SUCCESS;

    /* 去初始化SPI */
	ret = IoTSpiDeinit(spiId);
	if (ret != KP_ERR_SUCCESS) {
        printf("IoTSpiDeinit failed :%#x \r\n",ret); 
        return;
    }

    IotSpiCfgInitParam spiInitParam = {0};
	spiInitParam.isSlave = HI_FALSE;
	IotSpiCfgBasicInfo spiBasicInfo = {0};
	spiBasicInfo.cpol = HI_SPI_CFG_CLOCK_CPOL_0;
	spiBasicInfo.cpha = HI_SPI_CFG_CLOCK_CPHA_0;
	spiBasicInfo.framMode = HI_SPI_CFG_FRAM_MODE_MOTOROLA;
	spiBasicInfo.dataWidth = HI_SPI_CFG_DATA_WIDTH_E_8BIT;
	spiBasicInfo.endian = HI_SPI_CFG_ENDIAN_LITTLE;
	spiBasicInfo.freq = 2000000; /* defaul freq 2000000 Hz */
    
    //test_spi_printf("app_demo_spi_test_cmd_mw_sr Start");
	
	ret = IoTSpiInit(spiId, spiInitParam, &spiBasicInfo);
    if (ret != KP_ERR_SUCCESS) {
        printf("IoTSpiInit failed :%#x \r\n",ret); 
        return;
    }
    
	IoTSpiSetLoopBackMode(spiId, HI_FALSE);
    
	osDelay(100);
    
	IoTSpiSetIrqMode(spiId, HI_FALSE);
	IoTSpiSetDmaMode(spiId, HI_FALSE);
    
	osDelay(100);

	printf("oled spi init success \r\n");
}

